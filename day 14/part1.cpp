map<pair<char, char>, char> pairs;

string step(string prev) {
    int n = prev.size();
    string next = "";
    FOR(i, n) {
        next += prev[i];
        auto cur_pair = MP(prev[i], prev[i + 1]);
        if (i + 1 < n && pairs.find(cur_pair) != pairs.end()) {
            next += pairs[cur_pair];
        }
    }
    return next;
}

