struct cuboid {
    int x1, x2, y1, y2, z1, z2;
};

const int n = 420; // hehe

void solve() {
    set<int> setx;
    set<int> sety;
    set<int> setz;

    vector<bool> is_on(n);
    vector<cuboid> cuboids(n);

    FOR(i, n) {
        string s; cin >> s;
        bool on = s == "on";
        cuboid c;
        cin >> c.x1 >> c.x2 >> c.y1 >> c.y2 >> c.z1 >> c.z2;

        c.x2++;
        c.y2++;
        c.z2++;

        setx.insert(c.x1);
        sety.insert(c.y1);
        setz.insert(c.z1);

        setx.insert(c.x2);
        sety.insert(c.y2);
        setz.insert(c.z2);

        cuboids[i] = c;
        is_on[i] = on;
    }

    debug(setx.size());
    debug(sety.size());
    debug(setz.size());

    vi xs(ALL(setx));
    vi ys(ALL(sety));
    vi zs(ALL(setz));

    sort(ALL(xs)); // just to be safe
    sort(ALL(ys));
    sort(ALL(zs));

    ll X = xs.size();
    ll Y = ys.size();
    ll Z = zs.size();


    vvvi grid(X);
    FOR(x, X) {
        grid[x] = vvi(Y);
        FOR(y, Y) {
            grid[x][y] = vi(Z);
        }
    }

    FOR(i, n) {
        cuboid c = cuboids[i];
        int xmin = lower_bound(ALL(xs), cuboids[i].x1) - xs.begin();
        int ymin = lower_bound(ALL(ys), cuboids[i].y1) - ys.begin();
        int zmin = lower_bound(ALL(zs), cuboids[i].z1) - zs.begin();

        int xmax = lower_bound(ALL(xs), cuboids[i].x2) - xs.begin();
        int ymax = lower_bound(ALL(ys), cuboids[i].y2) - ys.begin();
        int zmax = lower_bound(ALL(zs), cuboids[i].z2) - zs.begin();

        debug(i);

        for (int x = xmin; x < xmax; x++) {
            for (int y = ymin; y < ymax; y++) {
                for (int z = zmin; z < zmax; z++) {
                    grid[x][y][z] = is_on[i];
                }
            }
        }
    }

    ll ans = 0;
    for (int x = 0; x < X - 1; x++) {
        debug(x);
        for (int y = 0; y < Y - 1; y++) {
            for (int z = 0; z < Z - 1; z++) {
                if (grid[x][y][z]) {
                    ll sx = xs[x+1] - xs[x];
                    ll sy = ys[y+1] - ys[y];
                    ll sz = zs[z+1] - zs[z];
                    ans += sx * sy * sz;
                }
            }
        }
    }
    debug(ans);
}

